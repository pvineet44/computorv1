#include "libft.h"

int		ft_putendl_fd(char *s, int fd)
{
	int		i;
	char	c;

	i = 0;
	c = '\n';
	if (s == NULL)
		return (0);
	while (s[i] != '\0')
		write(fd, &s[i++], 1);
	write(fd, &c, 1);
	return (i);
}
